package Models;

public class Numero {

    public int id;
    public String icon;
    public String title;
    public String phone;
    public int type;


    public int getId() {
        return id;
    }

    public String getIcon() {
        return icon;
    }

    public String getTitle() {
        return title;
    }

    public String getPhone() {
        return phone;
    }

    public int getType() {
        return type;
    }
}

